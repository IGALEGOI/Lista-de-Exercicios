valorUm     = int(input('Digite o primeiro valor inteiro: '))
valorDois   = int(input('Digite o segundo valor inteiro: '))

print('Soma (x + y) =', valorUm + valorDois)
print('\nSubtração (x - y) =', valorUm - valorDois)
print('\nMultiplicação (x * y) =', valorUm * valorDois)
print('\nDivisão (x / y) =', valorUm / valorDois)
print('\nDivisão inteira (x // y) =', valorUm // valorDois)
print('\nExponenciação (x ** y) =', valorUm - valorDois)